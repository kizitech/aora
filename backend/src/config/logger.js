if (process.env.NODE_ENV === 'production') {
  // You can add external logging services here
  // For example: Loggly, Papertrail, etc.
}

// Create stream for Morgan HTTP logging
logger.stream = {
  write: (message) => {
    logger.info(message.trim());
  },
};

module.exports = logger;